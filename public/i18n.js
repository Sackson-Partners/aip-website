/* ══════════════════════════════════════
   i18n ENGINE (EN / FR only)
══════════════════════════════════════ */
const AIP_LANGS = ['en', 'fr'];

function getLang() {
  return localStorage.getItem('aip_lang') || 'en';
}

function setLang(lang) {
  if (!AIP_LANGS.includes(lang)) return;
  localStorage.setItem('aip_lang', lang);
  applyLang(lang);
}

function t(key, lang) {
  const entry = AIP_TRANSLATIONS[key];
  if (!entry) return '';
  return entry[lang] || entry['en'] || '';
}

function applyLang(lang) {
  const html = document.documentElement;
  html.setAttribute('dir', 'ltr');
  html.setAttribute('lang', lang);
  document.body.classList.remove('rtl');
  document.body.classList.add('ltr');

  /* Translate all [data-i18n] elements */
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key, lang);
    if (val) el.textContent = val;
  });

  /* Translate placeholders */
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    const val = t(key, lang);
    if (val) el.setAttribute('placeholder', val);
  });

  /* Update active state on switcher */
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

/* Boot on DOMContentLoaded */
document.addEventListener('DOMContentLoaded', () => {
  applyLang(getLang());

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.getAttribute('data-lang')));
  });
});
